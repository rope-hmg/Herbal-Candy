macro_rules! instrs {
    ($($name:expr => $type:ident : $bits:expr),* ,) => {
        pub static INSTRUCTIONS: &[instr::Instr_Descriptor] = &[
            $(instr::Instr_Descriptor::new($name, &layout::$type, $bits)),*
        ];
    };
}

